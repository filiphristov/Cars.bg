package com.company.Instruments;

public class Advertisement {
    private String brand;
    private String model;
    private String milige;
    private double price;
    private String owner;
    private String phoneNumber;

    public Advertisement(String brand, String model, String milige, double price, String owner, String phoneNumber) {
        this.brand = brand;
        this.model = model;
        this.milige = milige;
        this.price = price;
        this.owner = owner;
        this.phoneNumber = phoneNumber;
    }
}
